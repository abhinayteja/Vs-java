public class Lcount {
    public static void main(String[] args) {
        int count = 1;

        while (count != 6) {
            System.out.println(count);
            count++;
        }
        System.out.println("\n");
        
        for(int i = 0;i != 10;i++){
            System.out.println(i);
        }
    }
}
